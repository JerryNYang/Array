fun main(args: Array<String>) {

    var x = 10
    var y = 20

    println(x)
    println(y)

    // Code to swap 'x' and 'y'
    x = x + y;  // x now becomes 30

    y = x - y;  // y becomes 10
    x = x - y;  // x becomes 20

    println(x)
    println(y)

    var i=5;
    println(i++);
    print(i--);

}